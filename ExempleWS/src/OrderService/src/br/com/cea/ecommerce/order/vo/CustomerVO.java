package br.com.cea.ecommerce.order.vo;

import br.com.cea.ecommerce.common.vo.IntegrationStatus;

public class CustomerVO {
	
	private String id;
	
	private String synchroId;
	
	private String email;
	
	private String firstName;
	
	private String lastName;
	
	private String documentType;
	
	private String document;
	
	private Long sequence;
	
	private boolean corporate;
	
	private String phone;
	
	private String corporateName;
	
	private String tradeName;
	
	private String corporateDocument;
	
	private String stateInscription;
	
	private String corporatePhone;
	
	private OrderVO order;
	
	private AddressVO address;
	
	private IntegrationStatus integrationStatus;

	public CustomerVO() {
		super();
		address = new AddressVO();
		address.setCustomer(this);
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSynchroId() {
		return synchroId;
	}

	public void setSynchroId(String synchroId) {
		this.synchroId = synchroId;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getDocumentType() {
		return documentType;
	}

	public void setDocumentType(String documentType) {
		this.documentType = documentType;
	}

	public String getDocument() {
		return document;
	}

	public void setDocument(String document) {
		this.document = document;
	}

	public Long getSequence() {
		return sequence;
	}

	public void setSequence(Long sequence) {
		this.sequence = sequence;
	}

	public boolean isCorporate() {
		return corporate;
	}

	public void setCorporate(boolean corporate) {
		this.corporate = corporate;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getCorporateName() {
		return corporateName;
	}

	public void setCorporateName(String corporateName) {
		this.corporateName = corporateName;
	}

	public String getTradeName() {
		return tradeName;
	}

	public void setTradeName(String tradeName) {
		this.tradeName = tradeName;
	}

	public String getCorporateDocument() {
		return corporateDocument;
	}

	public void setCorporateDocument(String corporateDocument) {
		this.corporateDocument = corporateDocument;
	}

	public String getStateInscription() {
		return stateInscription;
	}

	public void setStateInscription(String stateInscription) {
		this.stateInscription = stateInscription;
	}

	public String getCorporatePhone() {
		return corporatePhone;
	}

	public void setCorporatePhone(String corporatePhone) {
		this.corporatePhone = corporatePhone;
	}

	public OrderVO getOrder() {
		return order;
	}

	public void setOrder(OrderVO order) {
		this.order = order;
	}

	public AddressVO getAddress() {
		return address;
	}

	public void setAddress(AddressVO address) {
		this.address = address;
	}

	public IntegrationStatus getIntegrationStatus() {
		return integrationStatus;
	}

	public void setIntegrationStatus(IntegrationStatus integrationStatus) {
		this.integrationStatus = integrationStatus;
	}

}
