package br.com.cea.ecommerce.invoice.vo;

import java.util.Date;

public class InvoiceSynchro {
	
	private String orderId;
	private String invoiceNumber;
	private String synchroId;
	private int integrationId;
	private double totalValue;
	private Date issuanceDate;
	private String accessKey;
	private String serie;
	private String xml;
	
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getInvoiceNumber() {
		return invoiceNumber;
	}
	public void setInvoiceNumber(String invoiceNumber) {
		this.invoiceNumber = invoiceNumber;
	}
	public String getSynchroId() {
		return synchroId;
	}
	public void setSynchroId(String synchroId) {
		this.synchroId = synchroId;
	}
	public int getIntegrationId() {
		return integrationId;
	}
	public void setIntegrationId(int integrationId) {
		this.integrationId = integrationId;
	}
	public double getTotalValue() {
		return totalValue;
	}
	public void setTotalValue(double totalValue) {
		this.totalValue = totalValue;
	}
	public Date getIssuanceDate() {
		return issuanceDate;
	}
	public void setIssuanceDate(Date issuanceDate) {
		this.issuanceDate = issuanceDate;
	}
	public String getAccessKey() {
		return accessKey;
	}
	public void setAccessKey(String accessKey) {
		this.accessKey = accessKey;
	}
	public String getSerie() {
		return serie;
	}
	public void setSerie(String serie) {
		this.serie = serie;
	}
	public String getXml() {
		return xml;
	}
	public void setXml(String xml) {
		this.xml = xml;
	}
	
}
