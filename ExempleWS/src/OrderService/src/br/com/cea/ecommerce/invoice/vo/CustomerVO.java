package br.com.cea.ecommerce.invoice.vo;

import java.util.Date;

public class CustomerVO {
	
	private String id;
	
	private String email;
	
	private String firstName;
	
	private String lastName;
	
	private InvoiceVO invoice;
	
	private String documentType;
	
	private String document;
	
	private Long sequence;
	
	private String phone;
	
	private String corporateName;
	
	private String tradeName;
	
	private String corporateDocument;
	
	private String stateInscription;
	
	private String corporatePhone;
	
	private boolean corporate;
	
	private Date recordCreateDate;
	
	private Date recordUpdateDate;

	private String customerSynchroId;
	
	public CustomerVO(){
		super();		
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public InvoiceVO getInvoice() {
		return invoice;
	}

	public void setInvoice(InvoiceVO invoice) {
		this.invoice = invoice;
	}

	public String getDocumentType() {
		return documentType;
	}

	public void setDocumentType(String documentType) {
		this.documentType = documentType;
	}

	public String getDocument() {
		return document;
	}

	public void setDocument(String document) {
		this.document = document;
	}

	public Long getSequence() {
		return sequence;
	}

	public void setSequence(Long sequence) {
		this.sequence = sequence;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getCorporateName() {
		return corporateName;
	}

	public void setCorporateName(String corporateName) {
		this.corporateName = corporateName;
	}

	public String getTradeName() {
		return tradeName;
	}

	public void setTradeName(String tradeName) {
		this.tradeName = tradeName;
	}

	public String getCorporateDocument() {
		return corporateDocument;
	}

	public void setCorporateDocument(String corporateDocument) {
		this.corporateDocument = corporateDocument;
	}

	public String getStateInscription() {
		return stateInscription;
	}

	public void setStateInscription(String stateInscription) {
		this.stateInscription = stateInscription;
	}

	public String getCorporatePhone() {
		return corporatePhone;
	}

	public void setCorporatePhone(String corporatePhone) {
		this.corporatePhone = corporatePhone;
	}

	public boolean isCorporate() {
		return corporate;
	}

	public void setCorporate(boolean corporate) {
		this.corporate = corporate;
	}

	public Date getRecordCreateDate() {
		return recordCreateDate;
	}

	public void setRecordCreateDate(Date recordCreateDate) {
		this.recordCreateDate = recordCreateDate;
	}

	public Date getRecordUpdateDate() {
		return recordUpdateDate;
	}

	public void setRecordUpdateDate(Date recordUpdateDate) {
		this.recordUpdateDate = recordUpdateDate;
	}

	public String getCustomerSynchroId() {
		return customerSynchroId;
	}

	public void setCustomerSynchroId(String customerSynchroId) {
		this.customerSynchroId = customerSynchroId;
	}

}
