package br.com.cea.ecommerce.invoice.vo;

import java.util.Date;

public class PaymentVO {

	private String id;
	
	private String paymentSystem;
	
	private String paymentSystemName;
	
	private Double value;
	
	private Integer installments;
	
	private Double referenceValue;
	
	private String firstDigits;
	
	private String lastDigits;
	
	private String url;
	
	private String giftCardId;
	
	private String giftCardName;
	
	private String giftCardCaption;
	
	private String redemptionCode;
	
	private String group;
	
	private String billingAddress;
	
	private String connectResponses;
	
	private Date recordCreateDate;
	
	private Date recordUpdateDate;
	
	private InvoiceVO invoice;
	
	private ConnectorResponseVO connetorResponse;
	
	public PaymentVO(){
		super();
		connetorResponse = new ConnectorResponseVO();
		connetorResponse.setPayment(this);
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPaymentSystem() {
		return paymentSystem;
	}

	public void setPaymentSystem(String paymentSystem) {
		this.paymentSystem = paymentSystem;
	}

	public String getPaymentSystemName() {
		return paymentSystemName;
	}

	public void setPaymentSystemName(String paymentSystemName) {
		this.paymentSystemName = paymentSystemName;
	}

	public Double getValue() {
		return value;
	}

	public void setValue(Double value) {
		this.value = value;
	}

	public Integer getInstallments() {
		return installments;
	}

	public void setInstallments(Integer installments) {
		this.installments = installments;
	}

	public Double getReferenceValue() {
		return referenceValue;
	}

	public void setReferenceValue(Double referenceValue) {
		this.referenceValue = referenceValue;
	}

	public String getFirstDigits() {
		return firstDigits;
	}

	public void setFirstDigits(String firstDigits) {
		this.firstDigits = firstDigits;
	}

	public String getLastDigits() {
		return lastDigits;
	}

	public void setLastDigits(String lastDigits) {
		this.lastDigits = lastDigits;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getGiftCardId() {
		return giftCardId;
	}

	public void setGiftCardId(String giftCardId) {
		this.giftCardId = giftCardId;
	}

	public String getGiftCardName() {
		return giftCardName;
	}

	public void setGiftCardName(String giftCardName) {
		this.giftCardName = giftCardName;
	}

	public String getGiftCardCaption() {
		return giftCardCaption;
	}

	public void setGiftCardCaption(String giftCardCaption) {
		this.giftCardCaption = giftCardCaption;
	}

	public String getRedemptionCode() {
		return redemptionCode;
	}

	public void setRedemptionCode(String redemptionCode) {
		this.redemptionCode = redemptionCode;
	}

	public String getGroup() {
		return group;
	}

	public void setGroup(String group) {
		this.group = group;
	}

	public String getBillingAddress() {
		return billingAddress;
	}

	public void setBillingAddress(String billingAddress) {
		this.billingAddress = billingAddress;
	}

	public String getConnectResponses() {
		return connectResponses;
	}

	public void setConnectResponses(String connectResponses) {
		this.connectResponses = connectResponses;
	}

	public Date getRecordCreateDate() {
		return recordCreateDate;
	}

	public void setRecordCreateDate(Date recordCreateDate) {
		this.recordCreateDate = recordCreateDate;
	}

	public Date getRecordUpdateDate() {
		return recordUpdateDate;
	}

	public void setRecordUpdateDate(Date recordUpdateDate) {
		this.recordUpdateDate = recordUpdateDate;
	}

	public InvoiceVO getInvoice() {
		return invoice;
	}

	public void setInvoice(InvoiceVO invoice) {
		this.invoice = invoice;
	}

	public ConnectorResponseVO getConnetorResponse() {
		return connetorResponse;
	}

	public void setConnetorResponse(ConnectorResponseVO connetorResponse) {
		this.connetorResponse = connetorResponse;
	}

}
